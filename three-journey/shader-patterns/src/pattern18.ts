import {add, minus} from "./w-math";

let str = ""
for (let x = 0; x <= 1; x = add(0.1, x)) {
  for (let y = 0; y <= 1; y = add(0.1, y)) {
    let value: number | string = Math.max(Math.abs(minus(x, 0.5)), Math.abs(minus(y, 0.5)))
    if (value === 0) {
      value = "0.0";
    }
    str += " " + value
  }
  str += "\n"
}

console.table(str)

